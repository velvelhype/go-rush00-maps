package main

import "fmt"
import "piscine"

func output(str string, board [][]string) {
	fmt.Print(str);
	fmt.Println("=========\n");
	for h := 0; h < len(board); h++{ 
		for i := 0; i < len(board[h]); i++{ 
			for j := 0; j < len(board[h][i]); j++{
				fmt.Print(string(board[h][i][j]))
				fmt.Print(" ")
			}
		fmt.Println("")
	}
		piscine.Checkmate(board[h])
		fmt.Println("")
	}
}

